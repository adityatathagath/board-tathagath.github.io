# --- Data Loading and Caching ---
# Cache the data loading to improve performance on subsequent runs
@st.cache_data
def load_data(file):
    df = pd.read_excel(file, engine='openpyxl')
    df['Date'] = pd.to_datetime(df['Date'])
    return df

